#include "main.h"

/**
 * reversePrintRecursion - Prints a string in reverse using recursion
 * @s: The input string
 *
 * This function prints a given string in reverse order using recursion.
 */
void _print_rev_recursion(char *s)
{
if (*s == '\0')
{
return;
}

_print_rev_recursion(s + 1);
_putchar(*s);
}
